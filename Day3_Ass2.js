let grade=prompt("Enter your grade ");
switch (grade) {
    case 'A+':
        console.log("Pass with EXCELLENCE");
        break;

    case 'A':
        console.log("Pass with FIRST CLASS");
        break;

    case 'B+':
        console.log("Pass with SECOND CLASS");
        break;

    case 'B':
        console.log("PASSED");
        break;

    case 'C+':
        console.log("Passed with AGGREGATE");
        break;

    default:
        console.log("FAILED");
        break;
}

